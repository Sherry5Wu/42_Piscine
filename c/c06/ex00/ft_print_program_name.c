#include <unistd.h>

int	main(int ac, char **av)
{
	if (ac > 0)
	{
		while (*av[0])
		{
			write(1, av[0], 1);
			av[0]++;
		}
		write(1, "\n", 1);
	}
	else
		return (0);
}
