int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	result;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] == '\0' || s2[i] == '\0' || s1[i] != s2[i])
		{
			result = s1[i] - s2[i];
			return (result);
		}
		else
			i++;
	}
	result = 0;
	return (result);
}
